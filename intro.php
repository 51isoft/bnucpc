<p>
ACM/ICPC（ACM International Collegiate Programming Contest，国际大学生程序设计竞赛）是由国际计算机界历史悠久、颇具权威性的组织ACM（Association for Computing Machinery，美国计算机协会）主办的，世界上公认的规模最大、水平最高的国际大学生程序设计竞赛，其目的旨在使大学生运用计算机来充分展示自己分析问题和解决问题的能力。该项竞赛从1977年举办至今已历36届，一直受到国际各知名大学的重视，并受到全世界各著名计算机公司的高度关注，在过去十几年中，APPLE、AT&amp;T、MICROSOFT和IBM等世界著名信息企业分别担任了竞赛的全球赞助商。可以说，ACM国际大学生程序设计竞赛已成为世界各国大学生最具影响力的国际级计算机类的赛事， 是广大爱好计算机编程的大学生展示才华的舞台，是著名大学计算机教育成果的直接体现，是信息企业与世界顶尖计算机人才对话的最好机会。
</p>
<p>
该项竞赛分区域预赛和国际决赛两个阶段进行，各预赛区第一名自动获得参加世界决赛的资格，世界决赛安排在每年的2～5月举行，而区域预赛安排在上一年的9～12月在各大洲举行。
</p>
<p>
ACM/ICPC的区域预赛是规模很大、范围很广的赛事。仅在2009年参加区域预赛的队伍就有来自82个国家（地区），1931所高校的7109支代表队，他们分别在200多个赛场中进行比赛，以争夺全球总决赛的103个名额，其激烈程度可想而知。2010年第35届ACM/ICPC亚洲赛区预赛共设了哈尔滨、天津、杭州、成都、福州、高雄、大田、阿姆里塔普、河内、东京、雅加达、达卡、吉隆坡、坎普尔、德黑兰等15个赛站，来自亚洲各国知名高校的各个代表队进行了激烈的角逐。
</p>
