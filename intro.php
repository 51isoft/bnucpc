中国大陆从1996年开始参加ACM/ICPC亚洲区预赛，至今已历15届。前六届赛区设在上海，由上海大学承办；2002年分设北京和西安赛区，分别由清华大学和西安交通大学承办；2003年设北京和广州赛区，分别由清华大学和中山大学承办。2004年设北京、上海赛区，分别由北京大学和上海交通大学举办。2005年设北京、杭州、成都赛区，分别由北京大学、浙江大学和四川大学承办。2006年设北京、西安、上海赛区，分别由清华大学，西安电子科技大学，上海大学承办。2007年设南京、长春、北京、成都四个赛区，分别由南京航空航天大学、吉林大学、北京航空航天大学和西华大学承办。2008年设立了五个赛区，分别是哈尔滨赛区（由哈尔滨工程大学承办）、北京赛区（由北京交通大学承办）、合肥赛区（由中国科学技术大学承办）、杭州赛区（由杭州电子科技大学承办）、成都赛区（由西南民族大学承办）。2009年在中国大陆地区有五个赛区，分别是哈尔滨赛区（由哈尔滨工业大学承办）、合肥赛区（由中国科学技术大学承办）、宁波赛区（由浙江大学宁波理工学院承办）、上海赛区（由东华大学承办）和武汉赛区（由武汉大学承办）。2010年有五个赛区，分别是哈尔滨赛区（由哈尔滨工程大学承办）、天津赛区（由天津大学承办）、杭州赛区（由浙江理工大学承办）、福州赛区（由福州大学承办）和成都赛区（由四川大学承办）。2011年有五个赛区，分别是大连赛区（由大连理工大学承办）、上海赛区（由复旦大学承办）、北京赛区（由北京邮电大学承办）、福州赛区（由福州师范大学承办）和成都赛区（由成都东软学院承办）
